package com.example.KhanyisileMndaweni.entities;

import jakarta.persistence.*;
import lombok.*;

@Entity(name = "Users")
@Data
@NoArgsConstructor
@AllArgsConstructor
public class User {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;
    private String username;
    private String password;
    private String email;
    private String role;
}